    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        panCalc.setVisible(true);
        float num = Float.parseFloat(txtNum.getValue().toString());
        int resto = (int)num%2;
        int cubo = (int)Math.pow(num,3);
        float raizc = (float)Math.cbrt(num);
        float raizq = (float) Math.sqrt(num);
        int abs = (int)Math.abs(num);
        lblResto.setText(Integer.toString(resto));
        lblCubo.setText(Integer.toString(cubo));
        
        //Raiz Quadrada com formatacao e 3 casas decimais
        lblRaizQ.setText(String.format("%.3f",raizq));
        //Raiz Cubica com formatacao e 4 casas decimais
        lblRaizC.setText(String.format("%.3f",raizc));
        
        lblAbs.setText(Integer.toString(abs));
           
    }//GEN-LAST:event_jButton1ActionPerformed

